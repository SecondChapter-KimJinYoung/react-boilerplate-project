// 비밀번호
export const PASSWORD_REGEX = {
  // 정규식 적용 안 함
  NONE: /.*/,
  // 최소 8자, 영문+숫자 (특수문자 선택사항)
  BASIC: /^(?=.*[A-Za-z])(?=.*\d).{8,}$/,
  // 최소 8자, 영문+숫자+특수문자
  STRONG: /^(?=.*[A-Za-z])(?=.*\d)(?=.*[@$!%*#?&])[A-Za-z\d@$!%*#?&]{8,}$/,
  // 최소 8자, 대문자+소문자+숫자+특수문자
  VERY_STRONG: /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*#?&])[A-Za-z\d@$!%*#?&]{8,}$/,
  // 연속된 문자 3개 이상 불가 (예: aaa, 111)
  NO_CONSECUTIVE: /^(?!.*(.)\1{2,}).+$/,
} as const;

